"""Markdown export utilities for hotspot analysis results.

Provides strongly-typed export functionality to generate markdown reports
from hotspot analysis data and LLM conversation history.
"""

from pathlib import Path

from langchain_core.messages import AIMessage

from integrations.github.models import FileHotspot, HotspotAnalysisResult

from ..models import ExportMetadata


def format_hotspot_table(hotspots: list[FileHotspot]) -> str:
    """Format hotspot data as a markdown table.

    Args:
        hotspots: List of file hotspots to format

    Returns:
        Markdown table string
    """
    if not hotspots:
        return "_No hotspots found matching the criteria._\n"

    # Table header
    lines = [
        (
            "| File Path | Changes | Additions | Deletions | Churn Score "
            "| Authors | First Changed | Last Changed |"
        ),
        (
            "|-----------|---------|-----------|-----------|-------------|"
            "---------|---------------|--------------|"
        ),
    ]

    # Table rows
    for hotspot in hotspots:
        first_changed = (
            hotspot.first_changed.strftime("%Y-%m-%d")
            if hotspot.first_changed
            else "N/A"
        )
        last_changed = (
            hotspot.last_changed.strftime("%Y-%m-%d") if hotspot.last_changed else "N/A"
        )

        # Format churn score based on type (handle both int and float)
        if isinstance(hotspot.churn_score, float):
            churn_str = f"{hotspot.churn_score:.2f}"
        else:
            churn_str = str(hotspot.churn_score)

        row = (
            f"| `{hotspot.file_path}` | {hotspot.change_count} | "
            f"{hotspot.total_additions} | {hotspot.total_deletions} | "
            f"{churn_str} | {hotspot.unique_authors} | "
            f"{first_changed} | {last_changed} |"
        )
        lines.append(row)

    return "\n".join(lines) + "\n"


